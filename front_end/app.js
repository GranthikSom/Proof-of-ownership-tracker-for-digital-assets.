let web3 = new Web3(Web3.givenProvider || "http://localhost:8545");
let contract = new web3.eth.Contract(contractABI, contractAddress);

async function registerAsset() {
    const accounts = await web3.eth.requestAccounts();
    let name = document.getElementById("name").value;
    let description = document.getElementById("description").value;
    let assetURI = document.getElementById("assetURI").value;
    let contentHash = document.getElementById("contentHash").value;
    let isTransferable = document.getElementById("isTransferable").checked;

    contract.methods.registerAsset(name, description, assetURI, contentHash, isTransferable)
        .send({ from: accounts[0] })
        .then(() => alert("Asset Registered!"));
}

async function transferAsset() {
    const accounts = await web3.eth.requestAccounts();
    let assetId = document.getElementById("transferAssetId").value;
    let newOwner = document.getElementById("newOwner").value;

    contract.methods.transferAsset(assetId, newOwner)
        .send({ from: accounts[0] })
        .then(() => alert("Asset Transferred!"));
}